<h2>Какие проблемы решают базы данных?</h2>
<ul>
	<li>структурированное хранения данных для быстрого и удобного доступа к ним</li>
	<li>хранение больших объемов данных (документы, информация)</li>
	<li>быстрый поиск по данным и мгновенное получение результатов в любом формате</li>
	<li>ограничение доступа к данным для каждого из сотруников компании, отвечающие за свои разделы информации</li>
	<li>получение статистических данных, отчетов, выручки и прибыли компании в отдельных сегментах бизнеса для удобного анализа (&laquo;сквозная аналитика&raquo;)</li>
</ul>
